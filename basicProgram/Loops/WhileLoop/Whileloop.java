package Loops.WhileLoop;

public class Whileloop {
     public static void main(String[] args) {
          short number = 0;


          /**
           * while(condition) {
           * // body of the loop
           * }
           */
          while (number < 11) {
               System.out.println(++number + " JAVA dsa");
          }
          number++;
        
     } 
}