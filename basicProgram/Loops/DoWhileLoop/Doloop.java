package Loops.DoWhileLoop;

public class Doloop {
     public static void main(String[] args) {
          short number = 0;

          /**
           * do {
           * // body of loop;
           * }
           * while (condition);
           */

          do {
               System.out.println(++number +" do While Loop");
          } while (number < 10);
     }
}
