package SecondDay;

public class DataType {
     public static void main(String[] args) {
          byte item1 = 10;
          short item2 = 20;
          int item3 = 30;
          long item4 = 90;
          float item5 = 200.50f;
          double item6 = 1000.00;
          char charecter = 'V';
          boolean Anser = true;
          String Name = "vikash Kushwaha";

          System.out.println("_____Primitive Datatype_____");
          System.out.println(
               "byte: " + item1 + "\n" + 
               "short: " + item2 +"\n"+ 
               "int: " + item3 +"\n"+ 
               "long: " + item4 +"\n"+ 
               "float: " + item5 +"\n"+ 
               "double: " + item6 +"\n"+ 
               "charecter: " + charecter +"\n"+ 
               "boolean: " + Anser +"\n"+ 
               "String Non-Primitive Datatype: " + Name + "\n"
          );
     }
}

